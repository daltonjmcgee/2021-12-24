  <h2>Model Controls & Information</h2>
  <ul>
    <li>Click and drag to rotate.</li>
    <li>Hold shift and drag to move chair position.</li>
    <li>Scroll to zoom in and out.</li>
    <li>
      There are two lights that can be adjusted on five metrics.
      <ul>
        <li>Instensity is the brightness of the Light.</li>
        <li>Color is the light's actual color in RBG (Red, Blue, Green).</li>
        <li>X is the left-right position.</li>
        <li>Y is the up-down position.</li>
        <li>
          Z is the close-far position, that is the "depth" of the Scene.
        </li>
      </ul>
    </li>
  </ul>
